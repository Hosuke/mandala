// 如來橫剖 · 自中線向一側（同錨）；兩側合 120＝身高，縱廣相稱，格網自方
export const 橫剖 = [
  { 段: '中至腋', 指: 12 },   // 胸半寬
  { 段: '膊', 指: 20 },       // 上臂
  { 段: '臂', 指: 16 },       // 前臂
  { 段: '手', 指: 12 },
];

// 坐像折算（p.0941c23–0942a01）
export const 坐像 = {
  盤線: 64, // 陰藏（60）下加四指，「平彈絣線」＝盤腿交線
  座面: 68, // 又加四指，「法身之下邊、寶座之上面」→ 坐高 68 指
  膝寬: 52, // 「與其趺坐雙膝外邊相去間闊分長短平等」＝盤線至白毫之距
  踵距: 4,  // 「兩踵相離分得四指」
};

// 尊格階梯（《續補》p.0947b11–c28、p.0949b24–c03）——搩度 × 12 ＝ 指
export const 搩度 = {
  佛: 10,   // 畫像百二十指
  菩薩: 10, // 具德菩薩同十搩
  佛母: 9,  // 108，縱＝廣（佛母／度母作十六歲少女相；羅漢獨覺轉輪王同級）
  忿怒: 8,  // 「一切威怒像，通作八搩度」（引《戒教王經》）
  矮身: 6,  // 72（吉祥王菩薩之類）
};

// 關節錨點：自頂之累計指數——骨架生成器之根
export function 錨點() {
  const m = { 頂: 0 };
  let y = 0;
  for (const { 段, 指 } of 縱剖) { y += 指; m[段] = y; }
  m.白毫 = m.額;   // 白毫中＝額段之末（自頂 12）
  m.陰藏 = m.臍至陰藏; // 自頂 60，全身正中
  return m;
}

// 誦戒：格網和數之斷言，破則擲
export function verify() {
  const 縱 = 縱剖.reduce((s, x) => s + x.指, 0);
  const 橫 = 2 * 橫剖.reduce((s, x) => s + x.指, 0);
  if (縱 !== 120) throw new Error(`量度破戒：縱剖 ${縱} ≠ 120`);
  if (橫 !== 120) throw new Error(`量度破戒：橫剖 ${橫} ≠ 120（縱廣不相稱）`);
  const m = 錨點();
  if (m.陰藏 !== 60) throw new Error(`量度破戒：陰藏 ${m.陰藏} 不居身半`);
  if (坐像.盤線 !== m.陰藏 + 4 || 坐像.座面 !== 坐像.盤線 + 4)
    throw new Error('量度破戒：坐像折算有誤');
  if (坐像.膝寬 !== 坐像.盤線 - m.白毫)
    throw new Error('量度破戒：膝寬不等於盤線至白毫之距');
}
verify();
